#include "libft.h"

void	*ft_memchr(const void *s, int c, size_t n)
{
	char	*str;
	size_t	i;

	str = (char *)s;
	i = -1;
	while (++i < n)
		if (*(str + i) == (char)c)
			return (str + i);
	return (NULL);
}
