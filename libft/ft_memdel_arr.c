#include "libft.h"

void	ft_memdel_arr(void ***ap)
{
	int i;

	i = -1;
	if (ap)
	{
		while ((*ap)[++i])
			free((*ap)[i]);
		free(*ap);
		*ap = NULL;
	}
}
